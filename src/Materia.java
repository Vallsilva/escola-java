
public class Materia {
	private String disciplina;
	private double nota;
	
	public void setMateria(String materia) {
		this.disciplina = materia;
	}
	public void setNota(double nota) {
		this.nota = nota;
	}
	public String getMateria() {
		return disciplina;
	}
	public double getNota() {
		return nota;
	}
}
